# In Ruby, there are different *types* of data:

# Numbers
puts 3
puts 3/7.0

# Non-numbers
puts "gitpod"
puts "3/5"

# True, False
puts true
puts false

# Nothing
puts nil

# Variables
x = 20
y = 5
amount = x + y
food = "tacos"
puts x/y
puts x * y
puts nil

# Combine strings and variables
puts food.class
puts amount.class
puts "We have #{amount} types of #{food} on today's menu"

# String manipulation
puts food.reverse
puts food.upcase